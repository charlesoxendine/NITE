import Foundation
import UIKit

extension UIColor {
    class func themeBlack() -> UIColor {
        return UIColor(red: 10/255, green: 9/255, blue: 8/255, alpha: 1)
    }
    
    class func themeLight() -> UIColor {
        return UIColor(red: 226/255, green: 219/255, blue: 221/255, alpha: 1)
    }
    
    class func themeRed() -> UIColor {
        return UIColor(red: 208/255, green: 83/255, blue: 83/255, alpha: 1)
    }
    
    class func themeBlueGray() -> UIColor {
        return UIColor(red: 78/255, green: 87/255, blue: 112/255, alpha: 1)
    }
}
